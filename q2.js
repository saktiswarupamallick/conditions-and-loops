// Example program to grade students based on marks

// Replace this with the actual marks of the student
var marks = 85;

// Determine the grade using if-else statements
var grade;

if (marks > 90) {
    grade = 'A';
} else if (marks > 70) {
    grade = 'B';
} else if (marks > 50) {
    grade = 'C';
} else {
    grade = 'F';
}

// Display the result
console.log("The student's grade is: " + grade);








